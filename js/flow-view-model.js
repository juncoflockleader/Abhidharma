function flowLanguageKey() {
    return getLang().fixed ? 'cn' : 'en';
}

function flowUiText(key) {
    const language = flowLanguageKey();
    return (FLOW_UI_TEXT[language] && FLOW_UI_TEXT[language][key]) || FLOW_UI_TEXT.cn[key] || key;
}

function flowTranslatedKey(key) {
    return key && String(key).startsWith('string_id_') ? t(key) : flowUiText(key);
}

function flowPatternReason(result) {
    const keyByBand = {
        registration: 'senseRegistrationReason',
        javana: 'senseJavanaReason',
        determining: 'senseDeterminingReason',
        ineffective: 'senseIneffectiveReason',
        veryClear: 'mindVeryClearReason',
        clear: 'mindClearReason',
        unclear: 'mindUnclearReason',
        veryUnclear: 'mindVeryUnclearReason',
    };
    return flowUiText(keyByBand[result.strengthBand && result.strengthBand.id] || 'selectPhase');
}

function flowScenarioReason(result) {
    if (!result.hasJavana) {
        return flowUiText('noJavanaControls');
    }
    if (result.scenario.arahant) {
        return flowUiText('javanaArahantReason');
    }
    return result.scenario.goodIntention
        ? flowUiText('javanaWholesomeReason')
        : flowUiText('javanaUnwholesomeReason');
}

function flowPhaseReason(phaseId, result) {
    if (phaseId === 10) {
        return flowScenarioReason(result);
    }
    if (phaseId === 6 || phaseId === 7 || phaseId === 8) {
        return result.scenario.likable === 3
            ? flowUiText('unpleasantResultReason')
            : flowUiText('pleasantResultReason');
    }
    if (phaseId === 11) {
        return flowUiText('registrationReason');
    }
    return flowPatternReason(result);
}

function buildFlowViewModel(state) {
    const result = resolveFlowScenario(state);
    const title = flowUiText(result.type === 'sense' ? 'senseDoor' : 'mindDoor');
    const patternLabel = flowTranslatedKey(result.pattern.classKey);
    const strengthLabel = `${flowUiText('currentLevel')} ${result.pattern.id} · ${patternLabel}`;
    const groups = result.groups.map((group) => ({
        ...group,
        label: flowUiText(FLOW_FAMILY_DEFINITIONS[group.family].labelKey),
        color: FLOW_FAMILY_DEFINITIONS[group.family].color,
    }));

    return {
        ...result,
        title,
        intro: flowUiText(result.type === 'sense' ? 'workspaceIntroSense' : 'workspaceIntroMind'),
        patternLabel,
        strengthLabel,
        groups,
        badges: [
            `${result.nodes.length} ${flowUiText('moments')}`,
            flowUiText(result.hasJavana ? 'hasJavana' : 'noJavana'),
            flowUiText(result.hasRegistration ? 'hasRegistration' : 'noRegistration'),
        ],
        overviewReasons: [flowPatternReason(result), flowScenarioReason(result)],
        presets: FLOW_PRESETS[result.type],
    };
}

function buildFlowPhaseDetail(viewModel, phaseId) {
    const normalizedId = Number(phaseId);
    const definition = FLOW_CITTA_BY_ID[normalizedId];
    if (!definition) {
        return null;
    }
    const occurrences = viewModel.nodes.filter((node) => node.phaseId === normalizedId);
    if (!occurrences.length) {
        return null;
    }
    const representative = occurrences[0];
    const firstIndex = representative.id;
    const lastIndex = occurrences[occurrences.length - 1].id;
    const previousNode = viewModel.nodes[firstIndex - 1] || null;
    const nextNode = viewModel.nodes[lastIndex + 1] || null;
    const descriptionKey = FLOW_PHASE_DESCRIPTION_KEYS[definition.key];
    const candidate = representative.candidate;

    return {
        id: normalizedId,
        title: flowTranslatedKey(definition.labelKey),
        familyLabel: flowUiText(FLOW_FAMILY_DEFINITIONS[definition.family].labelKey),
        description: flowUiText(descriptionKey),
        reason: flowPhaseReason(normalizedId, viewModel),
        occurrences: occurrences.length,
        moments: occurrences.map((node) => node.moment),
        candidateClass: candidate ? flowTranslatedKey(candidate.classKey) : null,
        candidateCittas: candidate ? candidate.cittaKeys.map(flowTranslatedKey) : [],
        previous: previousNode ? flowTranslatedKey(previousNode.definition.labelKey) : flowUiText('start'),
        next: nextNode ? flowTranslatedKey(nextNode.definition.labelKey) : flowUiText('end'),
    };
}

window.flowUiText = flowUiText;
window.buildFlowViewModel = buildFlowViewModel;
window.buildFlowPhaseDetail = buildFlowPhaseDetail;
